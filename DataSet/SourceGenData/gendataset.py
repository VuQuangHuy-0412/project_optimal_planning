import numpy as np

# generate random integer values
from random import seed
from random import randint
from google.colab import drive

n = int(input("Nhap n = "))

d = np.zeros((n, n))

# seed random number generator
seed(1)

for i in range(0, n):
  for j in range(0, n):
    d[i][j] = (abs(j - i)) * randint(1, 5)

sum = 0
for i in range(0, n):
  for j in range(0, n):
    sum += d[i][j]

D = sum // n

c = np.zeros((n, n))

for i in range(0, n):
  for j in range(0, n):
    c[i][j] = (abs(j - i)) * randint(1, 10)

drive.mount('/content/gdrive')
with open('/content/gdrive/My Drive/DataSetOP/data10.txt', 'w') as f:
   f.write(str(n))
   f.write(' ')
   f.write(str(round(D)) + '\n')
   for i in range (0, n):
     for j in range (0, n):
       f.write(str(round(d[i][j])))
       if (j == n-1):
         f.write('\n')
       else:
          f.write(' ')
   for i in range (0, n):
     for j in range (0, n):
       f.write(str(round(c[i][j])))
       if (j == n-1):
         f.write('\n')
       else:
          f.write(' ')